#Detect Suspicious Activity
#The system suspects fraud when: the same product appears more than 3 times, OR an order has status "Fraud".
#Write a program that scans the dataset and identifies suspicious products or orders. You must use nested loops to compare product occurrences. Print the Order_IDs of suspicious orders and the names of suspicious products.

suspicious_orders = []
suspicious_products = []

# Check for fraud status and product occurrences
for order in orders:
    if order[3] == "Fraud":
        suspicious_orders.append(order[0])
    product_count = 0
    for inner_order in orders:
        if inner_order[1] == order[1]:
            product_count += 1
    if product_count > 3:
        suspicious_products.append(order[1])

print("Suspicious Orders:", suspicious_orders)
print("Suspicious Products:", suspicious_products)

