#Generate a List of Unique Products
#Create a new list containing unique product names present in the dataset. You must implement this without using the set() function.
unique_products = []
for order in orders:
    product_name = order[1]
    if product_name not in unique_products:
        unique_products.append(product_name)

#Product Sales Analysis
#For each unique product, determine how many times it was ordered and the total revenue generated by that product. This requires nested loops.
product_sales = {}
for product in unique_products:
    product_sales[product] = {"count": 0, "revenue": 0}
    for order in orders:
        if order[1] == product:
            product_sales[product]["count"] += 1
            if order[3] == "Completed":
                product_sales[product]["revenue"] += order[2]

#Identify the Most Sold Product
#Based on the frequency of orders, determine which product appears the most in the dataset.
product_count = {}
for order in orders:
    product_name = order[1]
    if product_name in product_count:
        product_count[product_name] += 1
    else:
        product_count[product_name] = 1

most_sold_product = max(product_count, key=product_count.get)
print("Most Sold Product:", most_sold_product)

